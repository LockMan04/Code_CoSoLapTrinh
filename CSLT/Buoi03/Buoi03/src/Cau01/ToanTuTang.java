package Cau01;

public class ToanTuTang {

	public static void main(String[] args) {
		int i = 1;
		System.out.println("i :" + i);		
		System.out.println("++i :" + ++i);		// i = 2 Tăng trước i một đơn vị
		System.out.println("i++ :" + i++);		// i = 2 Tăng sau i một đơn vị
		System.out.println("i :" + i);
		System.out.println("--i :" + --i); 		// i = 2 Giảm trước i một đơn vị
		System.out.println("i-- :" + i--);		// i = 2 Giảm sau i một đơn vị
		System.out.println("i :" + i);	
	}

}
